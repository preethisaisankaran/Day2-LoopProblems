package revisionSums;

public class switchcase {
public static void main(String[] args){
    int d =1;
    switch (d){
        case 1:
            System.out.println("ONE");
            break;
        case 2:
            System.out.println("two");
            break;
        default :
            System.out.println("not in case");
            break;

    }
}
}
